package com.yhs.model;

public class Reward {
	private int RewardID;
	private String RewardContent;
	private String PunishContent;
	private String PersonNumber;
	private String RewardPs;

	public int getRewardID() {
		return RewardID;
	}

	public void setRewardID(int rewardID) {
		RewardID = rewardID;
	}

	public String getRewardContent() {
		return RewardContent;
	}

	public void setRewardContent(String rewardContent) {
		RewardContent = rewardContent;
	}

	public String getPunishContent() {
		return PunishContent;
	}

	public void setPunishContent(String punishContent) {
		PunishContent = punishContent;
	}

	public String getPersonNumber() {
		return PersonNumber;
	}

	public void setPersonNumber(String personNumber) {
		PersonNumber = personNumber;
	}

	public String getRewardPs() {
		return RewardPs;
	}

	public void setRewardPs(String rewardPs) {
		RewardPs = rewardPs;
	}

}
